function show() {

    let fruits = [
        "apple",
        "pear",
        "raspberry",
        "tomatos",
        "kiwi",
        "banana",
        "orange",
        "mandarin",
        "durian",
        "peache",
        "grap",
        "cherry",
    ];


    let display = 0;

    while (display < fruits.length) {

        alert(fruits[display]);
        display += 1;

    }
}

document.getElementById("run").addEventListener("click", function () {
    show();
})